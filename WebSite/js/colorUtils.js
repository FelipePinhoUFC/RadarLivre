var altColors = [
	md_indigo_50, 
	md_indigo_100, 
	md_indigo_200, 
	md_indigo_300, 
	md_indigo_400, 
	md_indigo_500, 
	md_indigo_600, 
	md_indigo_700, 
	md_indigo_800, 
	md_indigo_900
]

var altBegin = 0;
var altEnd = 50000;
var vectorColorsSize = altColors.length;

function getColorFromAlt(alt) {
	if(alt > altEnd)
		alt = altEnd;

	if(alt < 0)
		alt = 0;

	alt = alt - altBegin;
	var altInterval = altEnd - altBegin;

	var colorPos = parseInt(vectorColorsSize * alt / altInterval);
	return altColors[colorPos];
}